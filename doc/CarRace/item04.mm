MacOSXのJava7でのバグ
========================================

2012,10/28現在，MacOS XにJava7をインストールしている場合に
CarRaceのプログラムが動作しないというバグが確認されています．

この場合，MacOS Xに初めからインストールされているJava6を
使用して下さい．「アプリケーション」「ユーティリティ」
「Java Preferences」を起動して「概要」タブで表示される
「Java SE 6」どドラッグして一番上に持っていくとOKです．

MacOSXのJava7の対応策を調査中ですが，ちょっと難しそうです．

その他カーレース部門における質問などあれば，
プログラミングコンテスト実行委員<procon@do-johodai.ac.jp>
に連絡下さい．

